	NAME 			VARCHAR(200),
	LASTNAME 		VARCHAR(200),
	PHONE_NUMBER 	VARCHAR(200),
	DNI 			INT NOT NULL PRIMARY KEY,
	GENDER 			VARCHAR(200),
